/* JS não é uma linguagem fortemente tipada */

/* Exemplo de variável indefinida */
var vIndefinida;
console.log(typeof vIndefinida);

/* Exemplo de variável booleana */
var vBooleano = true;
console.log(typeof vBooleano);

/* Exemplo de variável alfanumérica (String) */
var vAlfaNumerica = "Geovana Ribeiro Silva";
console.log(typeof vAlfaNumerica);

/* Exemplo de variável numérica */
var vNumeroInteriro = -40;
console.log(typeof vNumeroInteriro);

/* Exemplo de variável de números fracionados */
var vNumeroFracionado = 40.55;
console.log(typeof vNumeroFracionado);

/* Exemplo de variável array */
var vArrayNums = [10, 20, 30];
console.log(typeof vArrayNums);


